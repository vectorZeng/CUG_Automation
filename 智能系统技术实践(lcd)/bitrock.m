function sys=mdlOutputs(t,x,u)
% %  This s-function contains 3 inputs, k_dc(sita_hp - sita_b),sita_b, and wob

WoB = u(3);
Rb=0.155575;vf=1;ucb=0.5;usb=0.8;yb=0.9;Dv = 10^-6;cb=50;
k3=907;


Tsb = WoB*Rb*usb;

% u(1) = k_dc(sita_hp - sita_b), u(2)=sita_b
Teb = k3*u(1) - cb*u(2);

if(abs(u(2))<Dv&&abs(Teb)<=Tsb)
    ToB = Teb;
elseif (abs(u(2))<Dv&&abs(Teb)>Tsb)
    ToB = Tsb*sign(Teb); 
else
    ub=ucb+(usb-ucb)*exp(-1*yb/vf*abs(u(2)));
    ToB = WoB*Rb*ub*sign(u(2));
end

ToB  = ToB; 
%/(1+0.02*sin(t))
sys=ToB;

